
# Python script that calculates the simple interest earned on an investment over a period of time

# variables for the calculation of the interest
principal = 1000
rate = float(0.05)
time = 3

# Formular for calculating the interest is (I = P * R * T)
interest = principal * rate * time

# The below prints the interest base on the formular and the value of the variables specified above
print("The simple interest is:", interest)
